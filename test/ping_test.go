func TestGetChecksum(t *testing.T) {
	check := internal.GetChecksum(
		[]byte{
			0x08, 0x00, 0x00, 0x00, 0x00, 0x01, 0x00, 0x0b, 0x61, 0x62, 0x63,
			0x64, 0x65, 0x66, 0x67, 0x68, 0x69, 0x6a, 0x6b, 0x6c, 0x6d, 0x6e,
			0x6f, 0x70, 0x71, 0x72, 0x73, 0x74, 0x75, 0x76, 0x77, 0x61, 0x62,
			0x63, 0x64, 0x65, 0x66, 0x67, 0x68, 0x69,
		})
	if check != 0x4d50 {
		t.Error()
	}

	check1Byte := internal.GetChecksum([]byte{0x00})
	if check1Byte != 0xFFFF {
		t.Error()
	}
}

func TestMarshalEchoRequest(t *testing.T) {
	expect := []byte{
		0x08, 0x00, 0x4d, 0x50, 0x00, 0x01, 0x00, 0x0b, 0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67, 0x68,
		0x69, 0x6a, 0x6b, 0x6c, 0x6d, 0x6e, 0x6f, 0x70, 0x71, 0x72, 0x73, 0x74, 0x75, 0x76, 0x77, 0x61,
		0x62, 0x63, 0x64, 0x65, 0x66, 0x67, 0x68, 0x69,
	}

	bytes := internal.MarshalEchoRequest(1, 11,
		[]byte{
			0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67, 0x68, 0x69, 0x6a,
			0x6b, 0x6c, 0x6d, 0x6e, 0x6f, 0x70, 0x71, 0x72, 0x73, 0x74,
			0x75, 0x76, 0x77, 0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67,
			0x68, 0x69})

	if !reflect.DeepEqual(expect, bytes) {
		t.Error()
	}
}

func TestUnmarshalEcho(t *testing.T) {
	expect := internal.ICMPEchoMessage{
		ICMPEchoHeader: internal.ICMPEchoHeader{Type: 8, Code: 0, Checksum: 0x4d50, Identifier: 0xff1e, SequenceNumber: 0xfe3e},
		Data: []byte{
			0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67, 0x68, 0x69, 0x6a,
			0x6b, 0x6c, 0x6d, 0x6e, 0x6f, 0x70, 0x71, 0x72, 0x73, 0x74,
			0x75, 0x76, 0x77, 0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67,
			0x68, 0x69},
	}

	marshaled := []byte{
		0x08, 0x00, 0x4d, 0x50, 0xff, 0x1e, 0xfe, 0x3e, 0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67, 0x68,
		0x69, 0x6a, 0x6b, 0x6c, 0x6d, 0x6e, 0x6f, 0x70, 0x71, 0x72, 0x73, 0x74, 0x75, 0x76, 0x77, 0x61,
		0x62, 0x63, 0x64, 0x65, 0x66, 0x67, 0x68, 0x69,
	}
	var unmarshaled internal.ICMPEchoMessage
	if ok := internal.UnmarshalEcho(marshaled, &unmarshaled); ok != nil {
		t.Error(ok)
	}

	if !reflect.DeepEqual(expect, unmarshaled) {
		t.Error("期待された値と一致しません。")
	}
}
